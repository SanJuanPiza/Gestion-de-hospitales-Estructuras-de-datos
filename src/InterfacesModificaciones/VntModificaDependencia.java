    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed

        if (jtNombre.getText().trim().length() == 0 || jcDependencia.getSelectedItem().toString().equals("Sin dependencias registradas"))
        {
            JOptionPane.showMessageDialog(this, "Ningun campo puede estar vacio para poder llevar a cabo esta acci√≥n", "Campos invalidos", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (MnpHDEP.verificarDuplicadosDependencias(jtNombre.getText().trim()))
        {
            JOptionPane.showMessageDialog(this, "No se permiten duplicados de nombre de dependencia, intenta con otro nombre", "No duplicados", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dependencia = jcDependencia.getSelectedItem().toString();
        String nombre = jtNombre.getText().trim();
        String selectedItem = jcTipo.getSelectedItem().toString();
        char tipo = selectedItem.charAt(0);
        NodoML n = MnpHDEP.encontrarDependencia(dependencia);

        if (n != null)
        {

            codigo.MnpHDEP.modificarDependencia(n, nombre, tipo);
            n.setEtiqueta(nombre);
        }
        JOptionPane.showMessageDialog(this, "La dependencia fue actualizada con el nombre: " + nombre + " y tipo: " + tipo, "Modificar dependencia", JOptionPane.INFORMATION_MESSAGE);

        CtrlInterfaz.limpia(jtNombre);
        CtrlInterfaz.habilita(false, jtNombre, jcTipo, jButton1);
        //validacion
        CtrlInterfaz.cambia(jcDependencia);

        jcDependencia.setModel(new javax.swing.DefaultComboBoxModel<>(codigo.MnpHDEP.mostrarDependenciasStCmbox()));

    }//GEN-LAST:event_jButton1ActionPerformed

