    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed

        if (jtNombre.getText().trim().length() == 0 || jtDireccion.getText().length() == 0 || jcDependencia.getSelectedItem().toString().equals("Sin dependencias registradas") || jcHospital.getSelectedItem().toString().equals("Sin hospitales registrados"))
        {
            JOptionPane.showMessageDialog(this, "Ningun campo puede estar vacio para poder llevar a cabo esta acci√≥n", "Datos invalidos", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (MnpHDEP.verificarNoDuplicadosDeHospital(jcDependencia.getSelectedItem().toString(), jtNombre.getText().toLowerCase().trim()) && !jtNombre.getText().equals(jcHospital.getSelectedItem().toString()))
        {

            JOptionPane.showMessageDialog(this, "No puedes ingresar una hospital con el mismo nombre en esta dependencia ", "Dato no valido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dependencia = jcDependencia.getSelectedItem().toString();
        String hospital = jcHospital.getSelectedItem().toString();
        String nombre = jtNombre.getText().trim();
        String dir = jtDireccion.getText().trim();
        int nivel = Integer.parseInt(jtNivel.getSelectedItem().toString());

        NodoML<Hospital> n = MnpHDEP.encontrarHospital(dependencia, hospital);

//        MnpHDEP.modificarHospital(n, nombre, dir, nivel);
//        n.setEtiqueta(nombre);
//        if (MnpHDEP.verificarRemo(nombre))
//        {
//            MnpHDEP.cambiaNombreRemo(hospital, nombre);
//        }
        NodoML<Hospital> tm = MnpHDEP.verificarTemporales(dependencia, hospital);
        if (tm != null)
        {

            if (nivel == 3 && tm.getObj().getNivel() != 3)
            {

                MnpHDEP.subirTercerNivel(dependencia, tm.getObj().getNombre());

            } else if (nivel != 3 && tm.getObj().getNivel() == 3)
            {
                MnpHDEP.bajarTercerNivel(dependencia, tm.getObj().getNombre());
                tm.getObj().setNivel(nivel);
            }

        } else if (n != null)
        {
            if (nivel == 3 && n.getObj().getNivel() != 3)
            {

                MnpHDEP.subirTercerNivel(dependencia, n.getEtiqueta());

            } else if (nivel != 3 && n.getObj().getNivel() == 3)
            {
                MnpHDEP.bajarTercerNivel(dependencia, n.getEtiqueta());
            }

        }
        if (n != null)
        {
            MnpHDEP.modificarHospital(n, nombre, dir, nivel);
            n.setEtiqueta(nombre);
        }

//        if (MnpHDEP.verificarRemoNodo(dependencia, nombre))
//        {
//            MnpHDEP.cambiaNombreRemo(hospital, nombre);
//        }
        JOptionPane.showMessageDialog(this, "El hospital fue actualizado con el nombre: " + nombre + "\n" + "Direccion: " + dir + "\n" + "Nivel: " + nivel, "Modificar hospital", JOptionPane.INFORMATION_MESSAGE);

        CtrlInterfaz.limpia(jtNombre, jtDireccion, jtNivel);
        CtrlInterfaz.habilita(false, jtDireccion, jtNivel, jtNombre, jcHospital, btnAceptar);
        //validacion
        CtrlInterfaz.cambia(jcDependencia);

        jcDependencia.setModel(new javax.swing.DefaultComboBoxModel<>(codigo.MnpHDEP.mostrarDependenciasStCmbox()));
        jcHospital.setModel(new javax.swing.DefaultComboBoxModel<>(codigo.MnpHDEP.mostrarHospitalesOrigenCmbox(jcDependencia.getSelectedItem().toString())));


    }//GEN-LAST:event_btnAceptarActionPerformed

