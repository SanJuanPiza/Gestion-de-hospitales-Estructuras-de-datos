    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
    {//GEN-HEADEREND:event_jButton1ActionPerformed
        if (jtNombre.getText().trim().length() == 0)
        {
            JOptionPane.showMessageDialog(this, "Ningun campo puede estar vacio para poder llevar a cabo esta acci√≥n", "Alta dependencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (MnpHDEP.verificarDuplicadosDependencias(jtNombre.getText().trim()))
        {
            JOptionPane.showMessageDialog(this, "No se permiten duplicados de nombre de dependencia, intenta con otro nombre", "No duplicados", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String nombre = jtNombre.getText().trim();
        String selectedItem = jcTipo.getSelectedItem().toString();
        char tipo = selectedItem.charAt(0);
        codigo.MnpHDEP.altaDependencia(nombre, tipo);
        JOptionPane.showMessageDialog(this, "La dependencia " + nombre + " fue dada de alta correctamente", "Alta dependencia", JOptionPane.INFORMATION_MESSAGE);

        CtrlInterfaz.limpia(jtNombre);
        CtrlInterfaz.habilita(false, jcTipo, jButton1);
        CtrlInterfaz.cambia(jtNombre);
        jcTipo.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]
        {
            "m", "e", "f"
        }));

    }//GEN-LAST:event_jButton1ActionPerformed

