    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed

        if (jtNombre.getText().trim().length() == 0 || jtDireccion.getText().trim().length() == 0 || jcDependencia.getSelectedItem().toString().equals("Sin dependencias registradas"))
        {
            JOptionPane.showMessageDialog(this, "Ningun campo puede estar vacio para poder llevar a cabo esta acci√≥n", "Alta hospital", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (MnpHDEP.verificarNoDuplicadosDeHospital(jcDependencia.getSelectedItem().toString(), jtNombre.getText().toLowerCase().trim()))
        {

            JOptionPane.showMessageDialog(this, "No puedes ingresar una hospital con el mismo nombre en esta dependencia ", "Dato no valido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dependencia = jcDependencia.getSelectedItem().toString();
        String nombre = jtNombre.getText().trim();
        String dir = jtDireccion.getText().trim();
        int nivel = Integer.parseInt(jtNivel.getSelectedItem().toString());

        codigo.MnpHDEP.altaHospital(nombre, dir, nivel, dependencia);
        if (nivel == 3)
        {
            MnpHDEP.subirTercerNivel(dependencia, nombre);
        }
        JOptionPane.showMessageDialog(this, "El hospital " + nombre + " fue dado de alta correctamente", "Alta hospital", JOptionPane.INFORMATION_MESSAGE);

        CtrlInterfaz.limpia(jtNombre, jtDireccion, jtNivel);
        CtrlInterfaz.habilita(false, jtDireccion, jtNivel, btnAceptar, jtNombre);
        //validar
        CtrlInterfaz.cambia(jcDependencia);
        jcDependencia.setModel(new javax.swing.DefaultComboBoxModel<>(codigo.MnpHDEP.mostrarDependenciasStCmbox()));

    }//GEN-LAST:event_btnAceptarActionPerformed

    private void jtNombreKeyPressed(java.awt.event.KeyEvent evt)//GEN-FIRST:event_jtNombreKeyPressed
    {//GEN-HEADEREND:event_jtNombreKeyPressed
        if (evt.getKeyChar() == '\n' && MnpHDEP.verificarNoDuplicadosDeHospital(jcDependencia.getSelectedItem().toString(), jtNombre.getText().toLowerCase().trim()))
        {

            JOptionPane.showMessageDialog(this, "No puedes ingresar una hospital con el mismo nombre en esta dependencia ", "Dato no valido", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Validaciones.enterCadenaNoVacia(this, evt, jtNombre, jtDireccion);
    }//GEN-LAST:event_jtNombreKeyPressed

